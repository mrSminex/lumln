"""
Утилиты для работы с московским временем (МСК, UTC+3, без перехода на летнее время).

Все «отложенные сообщения» и проверки времени в боте и админ-панели
должны ориентироваться на московское время, независимо от того,
в каком часовом поясе настроен сам сервер (часто это UTC).
"""

from datetime import datetime
from zoneinfo import ZoneInfo

MOSCOW_TZ = ZoneInfo("Europe/Moscow")


def now_msk() -> datetime:
    """
    Текущее время по Москве как «наивный» datetime (без tzinfo).

    Наивный формат нужен, чтобы его можно было напрямую сравнивать
    и форматировать так же, как значения send_at, которые вводит
    администратор (он вводит московское время как обычную строку).
    """
    return datetime.now(MOSCOW_TZ).replace(tzinfo=None)
